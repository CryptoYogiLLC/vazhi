def process_batch(batch_num):
    """Process a single batch"""
    input_file = INPUT_DIR / f"batch_{batch_num:02d}.json"
    output_file = OUTPUT_DIR / f"batch_{batch_num:02d}_tamil.json"

    if not input_file.exists():
        return 0, 0

    with open(input_file, 'r', encoding='utf-8') as f:
        data = json.load(f)

    samples = data.get("samples", [])
    translated = []
    total_pct = 0

    for sample in samples:
        result = translate_to_tamil(sample)
        translated.append(result)
        pct = calculate_tamil_pct(result['output'])
        total_pct += pct

    avg_pct = total_pct / len(translated) if translated else 0

    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(translated, f, ensure_ascii=False, indent=2)

    status = "âœ“" if avg_pct >= 50 else "â–³"
    print(f"Batch {batch_num:02d}: {len(translated)} samples, à®¤à®®à®¿à®´à¯: {avg_pct:.1f}% {status}")
    return len(translated), avg_pct


