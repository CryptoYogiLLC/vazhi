def calculate_tamil_pct(text):
    """Calculate Tamil character percentage"""
    tamil = sum(1 for c in text if '\u0B80' <= c <= '\u0BFF')
    total = len(text.replace(' ', '').replace('\n', '').replace('\t', ''))
    return (tamil / total * 100) if total > 0 else 0

